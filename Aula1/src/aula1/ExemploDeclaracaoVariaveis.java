package aula1;

public class ExemploDeclaracaoVariaveis {
  public static void main(String[] args) {
	  int valorInteiro = 1000;
	  byte valorByte = 100;
	  double valorDouble = 0.856;
	  char primeiroChar = 'P';
	  char segundoChar = 30;
	  String terceiroChar = "Meu Caractere";
	  String meuNome = "Diego Ramos";
	  System.out.println(valorInteiro);
	  System.out.println(valorByte);
	  System.out.println(valorDouble);
	  System.out.println(primeiroChar);
	  System.out.println(segundoChar);
	  System.out.println(terceiroChar);
	  System.out.println(meuNome);
  }
}
